    /**
     * Handle the Order "updated" event.
     *
     * @param  \App\Models\Order  $order
     * @return void
     */
    public function updated(Order $order)
    {
        if ($order->getStatus() == 'Confirmed')
        {
            $wallet = new Wallet();
            $wallet->name = $order->token->name;
            $wallet->short_name = $order->token->short_name;

            $order->user->wallets()->save($wallet, ['balance' => $order->price]);
        }
    }

